package com.example.smartrecyclingapp;

public class LocationMap {
    public String name;     // Tên địa điểm
    public double latitude; // Vĩ độ
    public double longitude;// Kinh độ

    public LocationMap(String name, double latitude, double longitude) {
        this.name = name;
        this.latitude = latitude;
        this.longitude = longitude;
    }
}

